/* Return a 32-bit CRC of the contents of the buffer. */

unsigned long 
crc32(unsigned char *s, int len)
{
  int i;
  unsigned long crc32val;
  
  crc32val = 0;
  for (i = 0;  i < len;  i++) {
      crc32val = crc32_tab[(crc32val ^ s[i]) & 0xff] ^ (crc32val >> 8);
  }
  return crc32val;
}

